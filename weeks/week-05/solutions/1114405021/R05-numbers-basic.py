# R05. 數字基礎：四捨五入、進制、格式化（3.1–3.4）
# round / Decimal / format / bin / oct / hex

from decimal import Decimal, localcontext
import math

# ── 3.1 四捨五入 ──────────────────────────────────────
# round(數值, 小數位數) 的核心規則：
# - 第二個參數為正數：保留到小數點後 N 位
# - 第二個參數為 0 或省略：四捨五入到整數
# - 第二個參數為負數：往整數位（十位、百位、千位）做四捨五入
print(round(1.27, 1))  # 1.3
print(round(1.25361, 3))  # 1.254

# Python 對 .5 採用「銀行家捨入（round half to even）」：
# - 0.5 介於 0 與 1 之間，最近的偶數是 0
# - 2.5 介於 2 與 3 之間，最近的偶數是 2
# 這種做法可在大量統計時降低系統性偏差。
print(round(0.5))  # 0（銀行家捨入，取最近偶數）
print(round(2.5))  # 2

a = 1627731
# round(a, -2) 代表「對百位」做四捨五入：
# -2 對應百位，-1 對應十位，-3 對應千位。
print(round(a, -2))  # 1627700（對百位四捨五入）

# ── 3.2 精確浮點數 ────────────────────────────────────
# 一般 float 使用二進位浮點表示，某些十進位小數無法精確表示。
# 因此 4.2 + 2.1 可能出現 6.300000000000001 這種看似「多一點點」的結果。
print(4.2 + 2.1)  # 6.300000000000001（有誤差）

# Decimal 以十進位語意運算，適合金融、金額、精度要求高的場景。
# 重點：建議用字串建立 Decimal（例如 "4.2"），避免先被 float 誤差污染。
da, db = Decimal("4.2"), Decimal("2.1")
print(da + db)  # 6.3（精確）

# localcontext() 可暫時調整 Decimal 設定（例如有效位數），
# 離開 with 區塊後會恢復，不會影響全域。
with localcontext() as ctx:
    ctx.prec = 3
    # 在 prec=3 下，結果會被限制成 3 位有效數字。
    print(Decimal("1.3") / Decimal("1.7"))  # 0.765

# math.fsum 修正大數+小數精度
# 一般 sum 在「超大數 + 很小數 + 抵銷」時可能丟失精度；
# fsum 使用更穩定的演算法來降低累積誤差。
print(math.fsum([1.23e18, 1, -1.23e18]))  # 1.0（正確）

# ── 3.3 數字格式化 ────────────────────────────────────
x = 1234.56789

# format(value, spec) 的 spec 可控制：
# - 小數位數（.2f）
# - 對齊與欄寬（>10.1f）
# - 千分位分隔（,）
# - 科學記號（e）
print(format(x, "0.2f"))  # '1234.57'
print(format(x, ">10.1f"))  # '    1234.6'
print(format(x, ","))  # '1,234.56789'
print(format(x, "0,.2f"))  # '1,234.57'
print(format(x, "e"))  # '1.234568e+03'

# ── 3.4 二八十六進制 ──────────────────────────────────
n = 1234

# bin / oct / hex 會保留前綴：0b / 0o / 0x，適合教學或識別用途。
print(bin(n), oct(n), hex(n))  # 0b10011010010 0o2322 0x4d2

# 若不想要前綴，可用 format(n, 'b'/'o'/'x')。
print(format(n, "b"), format(n, "x"))  # 10011010010 4d2

# int(字串, 基底) 可把不同進位字串轉回十進位整數。
print(int("4d2", 16), int("2322", 8))  # 1234 1234
