# R20. ChainMap 合併映射（1.20）
# 說明：本程式示範如何使用 collections 模組中的 ChainMap
# ChainMap 是一種用來「合併多個字典」的特殊資料結構
# 它會將多個字典「鏈接」在一起，形成一個統一的查找視圖
# 優點：不需要真的合併字典，節省記憶體，且可以各自獨立修改

# 匯入 collections 模組中的 ChainMap 類別
from collections import ChainMap

# ===== 1. 建立 ChainMap =====

# 定義兩個字典
a = {"x": 1, "z": 3}  # 字典 a：包含 x 和 z
b = {"y": 2, "z": 4}  # 字典 b：包含 y 和 z（z 與 a 衝突）

# 使用 ChainMap 將多個字典鏈接在一起
# 語法：ChainMap(字典1, 字典2, ...)
# ChainMap 會按照參數順序建立一個「映射鏈」
c = ChainMap(a, b)
# 這裡 a 在前面，b 在後面
# c 會同時包含 a 和 b 的所有鍵：x, z, y

# ===== 2. 鍵的查找規則 =====

# ChainMap 會從「第一個字典」開始依序查找鍵
# 找到第一個符合的鍵就會回傳值，不再繼續往下找
# 這稱為「短路原則」或「覆蓋原則」

c["x"]  # 結果：1
# 查找過程：a 有 'x' → 回傳 1，不再查找 b

c["z"]  # 結果：3（取到 a 的 z）
# 查找過程：
#   - 先在 a 中找 'z'，找到且值為 3 → 回傳 3
#   - b 中也有 'z'（值為 4），但因為 a 已經先找到，所以被「覆蓋」了

c["y"]  # 結果：2
# 查找過程：a 中沒有 'y' → 繼續在 b 中找，找到且值為 2 → 回傳 2

# ===== 重要特性 =====

# 1. ChainMap 是一個「視圖」，不是真正的複製
#    修改原始字典 a 或 b，ChainMap 會即時反映變化

# 2. ChainMap 中的鍵是「共享」的
#    若要修改值，必須直接修改原始字典（c.maps[0]['x'] = 999）

# 3. ChainMap 支援 in 運算子：'x' in c（檢查鍵是否存在）

# 4. ChainMap 的 maps 屬性可以取得底層的字典列表
#    c.maps → [{'x': 1, 'z': 3}, {'y': 2, 'z': 4}]

# ===== 使用情境 =====
# - 命令列引數 + 環境變數 + 預設值的合併
# - 多個設定檔的層級覆蓋（如：使用者設定 > 預設設定）
# - 當需要統一介面訪問多個字典，但不想真的合併時
