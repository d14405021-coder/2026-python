# U1. 解包失敗的原因：變數數量 ≠ 元素數量（1.1）
# 說明：本程式說明 Python 中「解包（Unpacking）」失敗的常見原因
# 解包是指將一個可疊代物件（如元組、列表）中的元素
# 「一次性」assign（分配）給多個變數的語法

# ===== 1. 正確的解包 =====

# 定義一個元組（tuple），包含兩個元素
p = (4, 5)

# 將元組的兩個元素分別解包給 x 和 y
# Python 會將 p[0]  assign 給 x，p[1] assign 給 y
x, y = p  # 成功！x = 4, y = 5
# 解包成功，因為元組有 2 個元素，變數也有 2 個，數量完全吻合

# ===== 2. 解包失敗的原因 =====

# 若嘗試用 3 個變數解包只有 2 個元素的元組，會發生 ValueError
# x, y, z = p  # ValueError：元素只有 2 個但變數要 3 個
#
# 錯誤原因：
# - p 元組只有 2 個元素：(4, 5)
# - 但試圖用 3 個變數接收：x, y, z
# - Python 不知道要把哪個值 assign 給第三個變數 z
# - 因此拋出 ValueError: not enough values to unpack (expected 3, got 2)
#
# 解決方法：
# 1. 確保變數數量與元素數量一致
# 2. 若元素過多，可以使用「星號表達式（*）」來收集剩餘元素：
#    x, y, *z = p  # x=4, y=5, z=[]（多餘的元素會放進 z）

# ===== 3. 解包的常見應用 =====

# 交換兩個變數的值（不需要臨時變數）
a = 10
b = 20
a, b = b, a  # 解包語法可以優雅地交換值：a=20, b=10


# 從函數回傳多個值（實際是回傳一個元組）
def get_point():
    return (100, 200)  # 回傳座標


x, y = get_point()  # 解包回傳的元組：x=100, y=200

# ===== 注意事項 =====
# - 解包時，Python 要求「左邊的變數數量」必須與「右邊的元素數量」完全相等
# - 若不確定元素數量，可以使用星號（*）來彈性接收
# - 常見錯誤訊息：ValueError: not enough values to unpack 或
#                 ValueError: too many values to unpack
