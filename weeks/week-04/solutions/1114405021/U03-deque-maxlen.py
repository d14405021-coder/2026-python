# U3. deque(maxlen=N) 為何能保留最後 N 筆（1.3）
# 說明：本程式說明 collections 模組中 deque 資料結構的「最大長度限制」功能
# deque（雙端佇列）是一種可以在兩端高效新增和移除元素的資料結構
# 當設定 maxlen 參數時，deque 會自動維護「固定長度」
# 新增元素時，若已達上限，會自動移除「最舊的元素」來容納新元素

# 匯入 collections 模組中的 deque 類別
from collections import deque

# ===== 1. 建立有最大長度限制的 deque =====

# 建立一個 deque，並設定 maxlen=3
# 語法：deque(maxlen=N)
# - maxlen：指定 deque 的最大容量
# - 當元素數量達到此上限後，新加入的元素會「擠掉」最舊的元素
q = deque(maxlen=3)

# ===== 2. 逐個加入元素 =====

# 使用 for 迴圈，依序將 1, 2, 3, 4, 5 加入 deque
for i in [1, 2, 3, 4, 5]:
    q.append(i)  # append() 從右側（末端）新增元素
    # 每次 append 後的狀態：
    # i=1: q = deque([1], maxlen=3)
    # i=2: q = deque([1, 2], maxlen=3)
    # i=3: q = deque([1, 2, 3], maxlen=3)  ← 已達上限
    # i=4: q = deque([2, 3, 4], maxlen=3)  ← 左側的 1 被移除
    # i=5: q = deque([3, 4, 5], maxlen=3)  ← 左側的 2 被移除

# ===== 3. 結果分析 =====

# 迴圈結束後，deque 的內容只剩 [3, 4, 5]
# 原因：deque 會自動維護「最近加入的 N 筆資料」
# - 最舊的 1 和 2 已被移除
# - 只保留了「最後三筆」：3, 4, 5
# 結果只剩 [3, 4, 5]

# ===== 4. deque 與普通列表的差異 =====

# 若使用普通列表，同樣的操作結果會保留所有元素：
# q_list = []
# for i in [1, 2, 3, 4, 5]:
#     q_list.append(i)
# 結果：q_list = [1, 2, 3, 4, 5]（5 個元素全部保留）

# deque 的優勢在於：
# - 固定長度，節省記憶體
# - 自動管理，無需手動刪除舊元素
# - 兩端操作都是 O(1) 時間複雜度

# ===== 5. 常見應用場景 =====

# - 移動視窗（Sliding Window）：計算最近 N 筆的平均值
# - 最近瀏覽記錄：保留使用者的最近 N 筆操作
# - 訊息佇列：限制緩衝區大小
# - 搜尋歷史：瀏覽器的「上一頁」、「下一頁」功能
