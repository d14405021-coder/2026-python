# R02. 正則表達式：搜尋、替換、旗標（2.4–2.8）
# 說明：本程式示範 Python 正規表達式（Regular Expression）的常見用法
# 包含：re.compile 編譯、findall/finditer 搜尋、sub 替換、 IGNORECASE/DOTALL 旗標

# 匯入 re 模組：Python 標準的正規表達式處理模組
import re

# ═══════════════════════════════════════════════════════════
# 2.4 匹配和搜尋
# ═══════════════════════════════════════════════════════════

# 定義測試文字，包含多個日期
text = "Today is 11/27/2012. PyCon starts 3/13/2013."

# 使用 re.compile() 編譯正規表達式
# 語法：re.compile(正規表達式 [, 旗標])
# 優點：預先編譯可提升重複使用時的效能
# 正規表達式：(\d+)/(\d+)/(\d+)
# - \d+：匹配一個或多個數字
# - /：匹配斜線（需要轉義）
# - (...)：捕獲群組，會被單獨提取出來
datepat = re.compile(r"(\d+)/(\d+)/(\d+)")
# 第一個群組：月份 (\d+)
# 第二個群組：日期 (\d+)
# 第三個群組：年份 (\d+)

# ── findall()：找出所有匹配 ──

# findall() 會找出所有匹配的內容
# 若有捕獲群組，回傳「群組內容」的列表（而非完整匹配）
print(datepat.findall(text))
# 結果：[('11', '27', '2012'), ('3', '13', '2013')]
# 說明：找到兩組日期，每組是一個包含三個群組內容的元組

# ── match()：從字串開頭匹配 ──

# match() 嘗試從「字串開頭」匹配正規表達式
# 若匹配成功，回傳 Match 物件；失敗則回傳 None
m = datepat.match("11/27/2012")
assert m is not None  # 斷言匹配成功

# group() 方法取得匹配結果
# - m.group(0) 或 m.group()：完整匹配內容（包含所有群組）
# - m.groups()：所有群組內容的元組
print(m.group(0), m.groups())  # '11/27/2012' ('11', '27', '2012')

# ── finditer()：找出所有匹配（逐一回傳） ──

# finditer() 與 findall() 類似，但回傳「Match 物件」的疊代器
# 每次迭代可取得更多匹配細節（如群組內容、位置等）
for m in datepat.finditer(text):
    # m.groups() 回傳包含所有群組的元組
    month, day, year = m.groups()
    # 將日期格式化為「年-月-日」
    print(f"{year}-{month}-{day}")
# 輸出：
# 2012-11-27
# 2013-3-13

# ═══════════════════════════════════════════════════════════
# 2.5 搜尋和替換
# ═══════════════════════════════════════════════════════════

# 使用 re.sub() 進行「搜尋並替換」
# 語法：re.sub(正規表達式, 替換內容, 原始字串)
# 替換內容中可使用「反向參照」\1, \2, \3 來引用群組
print(re.sub(r"(\d+)/(\d+)/(\d+)", r"\3-\1-\2", text))
# 結果：'Today is 2012-11-27. PyCon starts 2013-3-13.'
# 說明：
# - \1：對應第一個群組（月）
# - \2：對應第二個群組（日）
# - \3：對應第三個群組（年）
# 將「月/日/年」改為「年-月-日」格式

# ── 命名群組 ──

# 使用命名群組 (?P<name>...) 可以替群組取名字，增加可讀性
# 替換時使用 \g<name> 引用命名群組
print(
    re.sub(
        r"(?P<month>\d+)/(?P<day>\d+)/(?P<year>\d+)",
        r"\g<year>-\g<month>-\g<day>",
        text,
    )
)
# 說明：
# - (?P<month>\d+)：命名群組 month
# - (?P<day>\d+)：命名群組 day
# - (?P<year>\d+)：命名群組 year
# - \g<year>：引用命名群組 year
# 結果與上一行相同，但程式碼更具可讀性

# ── re.subn 回傳替換次數 ──

# subn() 與 sub() 相同，但會回傳「替換後的字串」和「替換次數」的元組
newtext, n = re.subn(r"(\d+)/(\d+)/(\d+)", r"\3-\1-\2", text)
print(f"替換了 {n} 次")  # 替換了 2 次
# newtext = 'Today is 2012-11-27. PyCon starts 2013-3-13.'
# n = 2（總共替換了 2 處）

# ═══════════════════════════════════════════════════════════
# 2.6 忽略大小寫
# ═══════════════════════════════════════════════════════════

# 定義包含不同大小寫的文字
s = "UPPER PYTHON, lower python, Mixed Python"

# 使用 re.IGNORECASE 旗標來忽略大小寫
# 旗標可以用 flags= 參數傳入，或在正規表達式字串後加上 re.IGNORECASE
print(re.findall("python", s, flags=re.IGNORECASE))
# 結果：['PYTHON', 'python', 'Python']
# 說明：不加旗標只會找到 'python'（小寫）
#       加了 IGNORECASE 後，會找到所有大小寫變體

# ═══════════════════════════════════════════════════════════
# 2.7 非貪婪（最短匹配）
# ═══════════════════════════════════════════════════════════

# 定義測試文字
text2 = 'Computer says "no." Phone says "yes."'

# ── 貪婪匹配（預設） ──

# 使用 (.*) 是「貪婪匹配」：會盡可能匹配更多內容
# .* 會匹配「除了換行外的任意字元」，且盡可能長
print(re.compile(r'"(.*)"').findall(text2))  # 貪婪：['no." Phone says "yes.']
# 結果：['no." Phone says "yes.']
# 說明：從第一個 " 匹配到最後一個 "，中間的全部吃進去
#       包含了不想要的 "no." Phone says "yes." 部分

# ── 非貪婪匹配（最短匹配） ──

# 在 * 後面加上 ? 變成 (.*?) 是「非貪婪匹配」
# 會盡可能匹配「最少」的內容，遇到第一個符合就停止
print(re.compile(r'"(.*?)"').findall(text2))  # 非貪婪：['no.', 'yes.']
# 結果：['no.', 'yes.']
# 說明：每次遇到 "..." 就停下來，分別抓到 'no.' 和 'yes.'
# 實用原則：若不確定，使用非貪懼匹配 (.*?) 更安全

# ═══════════════════════════════════════════════════════════
# 2.8 多行匹配（DOTALL）
# ═══════════════════════════════════════════════════════════

# 定義包含多行的註解文字
code = "/* this is a\nmultiline comment */"

# 預設的 . 不會匹配換行符 \n
# 使用 re.DOTALL（或 re.S）旗標，讓 . 可以匹配「任意字元包括換行」
print(re.compile(r"/\*(.*?)\*/", re.DOTALL).findall(code))
# 結果：[' this is a\nmultiline comment ']
# 說明：
# - /\*(.*?)\*/：匹配 /* ... */ 之間的內容（非貪懼）
# - re.DOTALL：讓 . 也能匹配換行符
# 若不加 DOTALL：結果會是 []（因為 . 無法跨行）
