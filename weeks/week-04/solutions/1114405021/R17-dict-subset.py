# R17. 字典子集（1.17）
# 說明：本程式示範如何利用字典推導式（Dictionary Comprehension）
# 從現有字典中篩選出一個「子集字典」

# ===== 1. 根據值（value）篩選 =====

# 定義一個股票價格字典：鍵（key）為股票代碼，值（value）為價格
prices = {"ACME": 45.23, "AAPL": 612.78, "IBM": 205.55}

# 使用字典推導式篩選價格大於 200 的股票
# 語法：{鍵: 值 for 鍵, 值 in 字典.items() if 條件}
# .items() 會把字典轉成鍵值對的可疊代物件
# 推導式會遍歷每個鍵值對，只保留滿足條件的元素
p1 = {k: v for k, v in prices.items() if v > 200}
# 結果：{'AAPL': 612.78, 'IBM': 205.55}
# 說明：ACME 的價格 45.23 不大於 200，因此被過濾掉

# ===== 2. 根據鍵（key）篩選 =====

# 定義一個集合（set），包含要保留的股票代碼名稱
tech_names = {"AAPL", "IBM"}
# 集合的特點：不重複、元素無順序

# 使用字典推導式，根據股票代碼是否在 tech_names 中來篩選
# 語法中的「k in tech_names」：檢查鍵是否在指定的集合內
p2 = {k: v for k, v in prices.items() if k in tech_names}
# 結果：{'AAPL': 612.78, 'IBM': 205.55}
# 說明：只有 AAPL 和 IBM 在 tech_names 中，ACME 不在所以被過濾掉

# ===== 技巧說明 =====
# 字典推導式是 Python 中建立新字典的簡潔方式
# 基本語法：{新的鍵: 新的值 for 項目 in 可疊代物件 [可選的 if 條件]}
# 若沒有 if 條件，就是單純的複製或轉換
# 若加上 if 條件，就可以用來篩選符合條件的鍵值對
