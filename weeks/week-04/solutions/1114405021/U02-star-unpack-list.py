# U2. 星號解包為何能處理「不定長」且結果固定是 list（1.2）
# 說明：本程式說明 Python 中的「星號表達式（Starred Expression）」
# 使用星號（*）可以讓解包語法「彈性處理」不定長度的資料
# 關鍵特性：星號收集的結果「永遠是 list」（即使是空的 list）

# ===== 1. 什麼是星號解包 =====

# 定義一個元組 record，包含姓名和電子郵件
record = ("Dave", "dave@example.com")

# 使用星號表達式（*phones）來解包
# 語法：變數1, 變數2, *收集變數 = 可疊代物件
# - name：會接收第一個元素（'Dave'）
# - email：會接收第二個元素（'dave@example.com'）
# - *phones：會接收「剩餘的所有元素」，並以 list 形式儲存
name, email, *phones = record

# 由於 record 只有 2 個元素，已經全部被 name 和 email 接收
# 因此 phones 收集不到任何元素，結果是「空列表」
# phones == [] 仍是 list（空列表）

# ===== 2. 星號解包的特性 =====

# 特性一：收集「剩餘」的元素
# 當變數數量少於元素數量時，星號用來收集多餘的元素
first, *middle, last = [1, 2, 3, 4, 5]
# first  → 1（第一個）
# middle → [2, 3, 4]（中間三個，被星號收集成 list）
# last   → 5（最後一個）

# 特性二：結果固定是 list
# 無論收集到多少元素，星號表達式的結果「永遠是 list」
# - 收集到多個元素：['b', 'c']
# - 收集到一個元素：['b']
# - 收集不到任何元素：[]（空列表）
empty, *rest = [1]  # empty=1, rest=[]

# 特性三：可以用於任何可疊代物件
# 星號表達式不只可以用於元組和列表，也可以用於字串、生成器等
*a, b = "hello"  # a=['h', 'e', 'l', 'l'], b='o'

# ===== 3. 星號的位置 =====

# 星號表達式可以出現在：
# - 中間：a, *b, c = [1, 2, 3, 4, 5]  # a=1, b=[2,3,4], c=5
# - 開頭：*a, b = [1, 2, 3]           # a=[1,2], b=3
# - 結尾：a, *b = [1, 2, 3]           # a=1, b=[2,3]

# ===== 注意事項 =====
# - 在一個解包語法中，只能有一個星號表達式
# - 若星號表達式收集不到任何元素，結果仍是空列表 []，不會是 None 或其他類型
# - 這是 Python 設計上的「一致性」：統一以 list 回傳，方便後續操作
