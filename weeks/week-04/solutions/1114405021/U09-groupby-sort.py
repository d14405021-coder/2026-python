# U9. groupby 為何一定要先 sort（1.15）
# 說明：本程式說明 Python 中 itertools.groupby 的一個重要特性
# groupby 只會將「相鄰且相同」的鍵值分在同一組
# 因此，若要正確分組，必須先「依照分組鍵值排序」

# 匯入 itertools 模組中的 groupby 函數
# groupby 可以將序列中「相鄰的相同鍵值」的元素收集在一起
from itertools import groupby

# 匯入 operator 模組中的 itemgetter 函數
# 用來指定分組的鍵值（key），比 lambda 更簡潔
from operator import itemgetter

# ===== 1. 測試資料 =====

# 定義一個未排序的資料列表
# 注意：07/02 的資料有兩筆，分別在第一筆和第三筆（中間被 07/01 隔開）
rows = [
    {"date": "07/02/2012", "x": 1},  # 第一筆：07/02
    {"date": "07/01/2012", "x": 2},  # 第二筆：07/01
    {"date": "07/02/2012", "x": 3},  # 第三筆：07/02
]

# ===== 2. 未排序就使用 groupby（錯誤做法） =====

# 沒排序：07/02 會被分成兩段（因為 groupby 只看「連續」）
# groupby 會將「相鄰且鍵值相同」的元素分在一組
# 由於 rows 未排序，07/02 的兩筆資料並不相鄰
for k, g in groupby(rows, key=itemgetter("date")):
    # k：分組的鍵值（即日期）
    # g：該組內所有元素的疊代器
    # list(g)：將疊代器轉為列表，方便查看
    list(g)

# 實際分組結果（未排序）：
# - 第一組：{'date': '07/02/2012', 'x': 1}（只有 1 筆）
# - 第二組：{'date': '07/01/2012', 'x': 2}（只有 1 筆）
# - 第三組：{'date': '07/02/2012', 'x': 3}（只有 1 筆）
#
# 問題：07/02 的兩筆資料被錯誤地分到了不同的組！
# 這不是我們想要的結果

# ===== 3. 先排序再使用 groupby（正確做法） =====

# 排序後：同 date 才會連在一起，分組才正確
# 使用 sort() 方法對 rows 依照日期排序
# 排序後的順序會變成：07/01, 07/02, 07/02
rows.sort(key=itemgetter("date"))

# 現在相同的日期會「連續排列」，groupby 才能正確分組
for k, g in groupby(rows, key=itemgetter("date")):
    list(g)

# 實際分組結果（已排序）：
# - 第一組：{'date': '07/01/2012', 'x': 2}（1 筆）
# - 第二組：{'date': '07/02/2012', 'x': 1}、{'date': '07/02/2012', 'x': 3}（2 筆）
#
# 正確！07/02 的兩筆資料被正確地分到了同一組

# ===== 4. 為何 groupby 只處理「相鄰」元素 =====

# groupby 的設計是一種「流式處理（Streaming）」的概念
# 它不需要事先知道所有資料，只需要逐一處理每一筆
# 當鍵值改變時，就開始一個新的組

# 優點：
# - 記憶體效率高（不需要將所有元素存入記憶體）
# - 適合處理「已排序的大型資料流」
#
# 缺點：
# - 若資料未排序，分組結果會錯誤
# - 無法自動排序（因為不知道何時才是「最後一個相同鍵值」）

# ===== 5. 重要提醒 =====

# 使用 groupby 前，請牢記以下兩點：
# 1. 資料必須「先排序」再傳入 groupby
# 2. 排序的鍵值「必須與分組的鍵值相同」
#
# 違反任一點都會導致分組結果錯誤

