# U4. heap 為何能高效拿 Top-N（1.4）
# 說明：本程式說明 Python 中 heapq 模組的「堆積（Heap）」資料結構
# 堆積是一種特殊的「二元樹」，具有以下核心性質：
# - 父節點的值永遠小於（或等於）子節點的值 → 稱為「最小堆積（Min Heap）」
# - h[0] 永遠是「目前所有元素中的最小值」
# 優點：取得最小值的時間複雜度是 O(1)，新增/刪除元素是 O(log n)

# 匯入 heapq 模組，提供一整套堆積操作的函數
import heapq

# ===== 1. 建立堆積 =====

# 定義一個普通列表（此時尚未符合堆積特性）
nums = [5, 1, 9, 2]

# 使用 heapify() 將普通列表轉換為「堆積結構」
# heapify() 會原地修改列表，將其重新排列成堆積
# 時間複雜度：O(n)，比逐個 heappush() 更有效率
h = nums[:]  # 先複製一份，避免修改原始列表
heapq.heapify(h)  # 現在 h 已經是合法的堆積結構

# ===== 2. 堆積的核心性質 =====

# h[0] 永遠是最小值（這是 heap 的核心性質）
# 這個特性是堆積最重要的特性，稱為「堆積屬性（Heap Property）」
# 無論堆積中有多少元素，取得最小值都是 O(1)

# ===== 3. 取出最小值（heappop） =====

# 使用 heappop() 取出並移除堆積的最小值
# 語法：heapq.heappop(堆積列表)
# - 會移除並回傳 h[0]（最小值）
# - 移除後，剩餘元素會自動重新排列，保持堆積結構
# - 時間複雜度：O(log n)
m = heapq.heappop(h)  # m 是目前堆積中的最小值
# 每次 pop 都拿到目前最小

# ===== 4. 為何 heap 能高效處理 Top-N =====

# 假設要從大量資料中找出「最小的 N 個」：
#
# 方法一（排序法）：
#   - 需要對所有資料排序：O(n log n)
#   - 適合少量資料或需要全部排序的情況
#
# 方法二（使用 heap）：
#   - 建立堆積：O(n)
#   - 取出 N 個最小值：O(N log n)
#   - 總複雜度：O(n + N log n)
#   - 當 N 遠小於 n 時，效率比排序法更好

# ===== 5. heapq 其他常用函數 =====

# heapq.heappush(heap, item)：將元素加入堆積，自動維持堆積結構
# heapq.heapreplace(heap, item)：彈出最小值後，立即加入新元素（原子操作）
# heapq.nlargest(n, iterable)：取得最大的 n 個元素
# heapq.nsmallest(n, iterable)：取得最小的 n 個元素（內部使用堆積實現）

# ===== 6. 堆積的視覺化 =====

# 例如：h = [1, 2, 9, 5] 轉換為堆積後
# 底層陣列：[1, 2, 9, 5]
# 樹狀結構：
#        1        ← h[0]，永遠是最小
#       / \
#      2   9      ← h[1], h[2]
#     /
#    5             ← h[3]
