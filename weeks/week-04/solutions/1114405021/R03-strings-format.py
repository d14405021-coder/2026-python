# R03. 字串清理、對齊、拼接與格式化（2.11–2.16）
# 說明：本程式示範 Python 中常用的字串處理技巧
# 包含：strip 清理、ljust/center/rjust 對齊、join 拼接、format 格式化、textwrap 斷行

# 匯入 textwrap 模組：用於將長文字依照指定寬度自動斷行
import textwrap

# ═══════════════════════════════════════════════════════════
# 2.11 清理字元
# ═══════════════════════════════════════════════════════════

# 定義一個包含前後空白和換行符的字串
s = "  hello world \n"

# strip()：移除「頭尾」的空白字元
# 預設移除：空白、換行(\n)、Tab(\t) 等空白字元
print(repr(s.strip()))  # 'hello world'
# 說明：移除了開頭的兩個空格和結尾的換行符
# repr() 會顯示字串的「官方表示」，清楚看到頭尾是否還有空白

# lstrip()：只移除「左側」（開頭）的空白字元
print(repr(s.lstrip()))  # 'hello world \n'
# 說明：只移除了開頭的空格，結尾的換行和空格保留

# rstrip()：只移除「右側」（結尾）的空白字元
# （此範例中未使用，但 lstrip 與 rstrip 是對稱的）

# strip() 可傳入自訂字元：要移除的字元集合
print("-----hello=====".strip("-="))  # 'hello'
# 說明：移除了開頭的 - 和結尾的 =
# 注意：會移除所有在字元集合中的字元，直到遇到不在集合中的字元為止

# ═══════════════════════════════════════════════════════════
# 2.13 字串對齊
# ═══════════════════════════════════════════════════════════

# 定義要對齊的字串
text = "Hello World"

# ── ljust()：左對齊 ──

# 語法：str.ljust(寬度 [, 填充字元])
# 將字串靠左對齊，不足的部分用填充字元補滿（預設是空白）
print(text.ljust(20))  # 'Hello World         '
# 說明：總寬度 20，"Hello World" 有 11 個字元
#       左對齊後，右側補上 9 個空白

# ── rjust()：右對齊 ──

# 語法：str.rjust(寬度 [, 填充字元])
# 將字串靠右對齊，不足的部分用填充字元補滿
print(text.rjust(20))  # '         Hello World'
# 說明：左側補上 9 個空白

# ── center()：居中對齊 ──

# 語法：str.center(寬度 [, 填充字元])
# 將字串居中對齊，不足的部分用填充字元補滿
print(text.center(20, "*"))  # '****Hello World*****'
# 說明：兩側各補上星號，左側 4 個，右側 5 個（可能差一個）

# ── format() 格式化對齊 ──

# 使用 format() 函數配合對齊符號進行對齊
# 語法：format(值, "對齊符號寬度")
# 對齊符號：< 左對齊、> 右對齊、^ 居中

# 字串居中對齊（總寬度 20）
print(format(text, "^20"))  # '    Hello World     '
# 等同於 text.center(20)

# 數字右對齊（總寬度 10，保留 2 位小數）
print(format(1.2345, ">10.2f"))  # '      1.23'
# 說明：
# - >：右對齊
# - 10：總寬度 10
# - .2f：浮點數，保留 2 位小數
# - 結果：前面補上空白至寬度 10

# ═══════════════════════════════════════════════════════════
# 2.14 合併拼接
# ═══════════════════════════════════════════════════════════

# 定義要拼接的單字列表
parts = ["Is", "Chicago", "Not", "Chicago?"]

# 使用 str.join() 將列表中的元素用「指定分隔符」連接成一個字串
# 語法：'分隔符'.join(列表)

# 以空白連接
print(" ".join(parts))  # 'Is Chicago Not Chicago?'
# 說明：用一個空白字元連接所有元素

# 以逗號連接
print(",".join(parts))  # 'Is,Chicago,Not,Chicago?'
# 說明：用逗號連接（注意：逗號後面不會自動加空白）

# ── 拼接不同類型的資料 ──

# 定義包含字串、整數、浮點數的列表
data = ["ACME", 50, 91.1]

# 直接 join() 會失敗，因為列表中有非字串元素
# 解決方法：先用生成器表達式將所有元素轉為字串
print(",".join(str(d) for d in data))  # 'ACME,50,91.1'
# 說明：str(d) 將每個元素轉為字串，再拼接

# ═══════════════════════════════════════════════════════════
# 2.15 插入變量
# ═══════════════════════════════════════════════════════════

# 定義變數
name, n = "Guido", 37

# 定義包含「替換欄位」的範本字串
# 替換欄位語法：{變數名}
s = "{name} has {n} messages."

# ── format() 方法 ──

# 使用 .format() 方法，傳入要替換的變數
print(s.format(name=name, n=n))  # 'Guido has 37 messages.'
# 說明：name 和 n 會被對應到 {name} 和 {n}

# ── format_map() 方法 ──

# format_map() 接受一個「字典」，直接用字典的值替換
# 適合動態傳入字典的情境
print(s.format_map(vars()))  # 'Guido has 37 messages.'
# 說明：vars() 回傳目前作用域的所有變數（包含 name 和 n）

# ── f-string（格式化字串字面值）──

# f-string 是 Python 3.6+ 引入的最簡潔格式化方式
# 在字串前加 'f' 或 'F'，大括號內可直接寫變數名或表達式
print(f"{name} has {n} messages.")  # f-string（最簡潔）
# 說明：直接在 {} 內寫變數名稱，最直觀、最易讀
# 這是目前最推薦的格式化方式

# ═══════════════════════════════════════════════════════════
# 2.16 指定列寬
# ═══════════════════════════════════════════════════════════

# 定義一個長字串（詩或歌詞）
long_s = (
    "Look into my eyes, look into my eyes, the eyes, "
    "not around the eyes, look into my eyes, you're under."
)

# 使用 textwrap.fill() 將長字串按照指定寬度斷行
# 語法：textwrap.fill(文字, 寬度)
print(textwrap.fill(long_s, 40))
# 結果：每行最多 40 個字元，自動在單字邊界處斷行

# ── 加上縮排 ──

# initial_indent 參數：設定「第一行」的縮排
# subsequent_indent 參數：設定「其餘行」的縮排（此範例未使用）
print(textwrap.fill(long_s, 40, initial_indent="    "))
# 說明：第一行前面會加上 4 個空白
# 常用於：將一段文字嵌入程式碼或 blockquote 中
