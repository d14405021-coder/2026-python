# R9. 兩字典相同點：keys/items 集合運算（1.9）

a = {'x': 1, 'y': 2, 'z': 3}
b = {'w': 10, 'x': 11, 'y': 2}

# 1) keys() 視圖可做集合運算
# 交集：兩個字典都存在的鍵
a.keys() & b.keys()

# 差集：在 a 有、但在 b 沒有的鍵
a.keys() - b.keys()

# 2) items() 也可做集合運算，但比的是「(key, value) 成對都相同」
# 這裡只有 ('y', 2) 會同時出現在 a 與 b。
a.items() & b.items()

# 3) 字典推導式 + 鍵差集：建立過濾後的新字典
# a.keys() - {'z', 'w'} 代表把 z 與 w 從 a 的鍵集合排除。
# 注意：w 不在 a 中也沒關係，集合差集會自然忽略。
c = {k: a[k] for k in a.keys() - {'z', 'w'}}

