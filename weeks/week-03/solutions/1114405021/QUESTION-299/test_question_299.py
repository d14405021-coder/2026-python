def count_inversions(trains):
    """
    計算逆序對數量
    
    ===================================================================
    邏輯：
    ===================================================================
    逆序對：位置 i < j 但數值 trains[i] > trains[j]
    
    每次相鄰交換恰好減少 1 個逆序對
    因此：最少交換次數 = 逆序對總數
    
    ===================================================================
    演算法：
    ===================================================================
    兩層迴圈檢查所有數對 (i, j)
    i < j 且 trains[i] > trains[j] → 計數
    
    ===================================================================
    時間複雜度：O(n²)
    空間複雜度：O(1)
    """
    count = 0  # 逆序對計數器
    n = len(trains)  # 排列長度
    
    # ───── 兩層迴圈檢查所有數對 ─────
    for i in range(n):
        for j in range(i + 1, n):
            # ───── 檢查是否為逆序對 ─────
            if trains[i] > trains[j]:
                # 找到一個逆序對，計數加 1
                count += 1
    
    return count

