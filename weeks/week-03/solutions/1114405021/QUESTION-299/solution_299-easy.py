"""
UVA 299 火車車廂置換 - 簡易版本

=================================================================
核心概念簡化：
=================================================================
✓ 計算「逆序對」= 計算最少交換次數
✓ 逆序對：i < j 但 arr[i] > arr[j] 的數對
✓ 每次相鄰交換恰好減少 1 個逆序對

=================================================================
最簡單的實現思路：
=================================================================
1. 兩層迴圈檢查所有數對
2. 如果發現逆序對就計數
3. 返回計數值

=================================================================
程式碼結構說明：
=================================================================
- count_inversions(): 計算逆序對的核心函數
- 主程式區塊：處理輸入輸出
- 輸入格式：第一行測資數量，之後每組測資兩行
- 輸出格式：每組測資一行結果

=================================================================
時間複雜度：O(L²) - L為列車長度，L≤50，效率足夠
=================================================================
"""

def count_inversions(trains):
    """
    計算逆序對數量 = 最少交換次數

    參數說明：
    trains (list): 火車車廂的排列列表，例如 [1, 3, 2]

    返回值：
    int: 逆序對的數量，等於最少交換次數

    演算法邏輯：
    - 使用兩層巢狀迴圈檢查所有可能的數對
    - 對於每對 (i, j) 其中 i < j：
      * 如果 trains[i] > trains[j]，表示這是一個逆序對
      * 累計逆序對的數量
    - 返回總計數

    範例：
    trains = [1, 3, 2]
    檢查 (0,1): 1 < 3，不計數
    檢查 (0,2): 1 < 2，不計數
    檢查 (1,2): 3 > 2，計數 +1
    返回 1

    trains = [4, 3, 2, 1]
    會找到 6 個逆序對：(4,3), (4,2), (4,1), (3,2), (3,1), (2,1)
    返回 6
    """
    count = 0  # 初始化逆序對計數器
    n = len(trains)  # 取得列車長度

    # ───── 第一層迴圈：遍歷每一個位置 i ─────
    for i in range(n):
        # ───── 第二層迴圈：從 i+1 開始檢查後面的元素 ─────
        # 為什麼從 i+1？因為只需要檢查 i < j 的組合，避免重複計算
        for j in range(i + 1, n):
            # ───── 檢查是否為逆序對 ─────
            # 如果前面的元素比後面的元素大，就是逆序對
            if trains[i] > trains[j]:
                count += 1  # 找到一個逆序對，計數器加一

    # 返回計算出的逆序對總數
    return count

# ============================================================================
# 主程式區塊 - 處理輸入輸出
# ============================================================================

# ───── 步驟 1：讀取測資數量 ─────
# 第一行輸入是一個整數，表示有多少組測試資料
n = int(input())

# ───── 步驟 2：處理每一組測試資料 ─────
# 使用迴圈處理 n 組測資
for _ in range(n):
    # ───── 讀取列車長度 L ─────
    # 第二行是列車長度 L (0 ≤ L ≤ 50)
    l = int(input())

    # ───── 讀取列車排列 ─────
    # 第三行是 L 個數字，表示車廂的當前排列
    if l > 0:
        # 如果 L > 0，讀取 L 個整數並轉換為列表
        trains = list(map(int, input().split()))
    else:
        # 如果 L = 0，排列為空列表
        trains = []

    # ───── 計算最少交換次數 ─────
    # 呼叫 count_inversions 函數計算逆序對數量
    swaps = count_inversions(trains)

    # ───── 輸出結果 ─────
    # 按照題目要求格式輸出結果
    print(f"Optimal train swapping takes {swaps} swaps.")
