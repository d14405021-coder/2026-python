"""
UVA 299 火車車廂置換 - 完整解決方案

=================================================================
問題描述 (UVA Online Judge - Problem 299)：
=================================================================
在鐵路站，車箱置換員需要重新排列火車車廂。
現在要寫一個程式計算：用最少的「相鄰車廂交換」操作，
將所有車廂排到正確順序（1 到 L）需要多少次交換。

=================================================================
核心概念 - 逆序對 (Inversion)：
=================================================================
「逆序對」是指在一個排列中，如果位置 i < j 但數值 a[i] > a[j]，
就稱 (a[i], a[j]) 為一個逆序對。

重要性質：
✓ 每次相鄰交換恰好減少 1 個逆序對
✓ 最少交換次數 = 輸入排列中逆序對的總數
✓ 計數逆序對就是計數最少交換次數

=================================================================
演算法說明：
=================================================================
時間複雜度：O(n²)（使用暴力法計數）
空間複雜度：O(1)（僅使用計數變數）

步驟：
1. 讀取測資數量 N
2. 對每組測資：
   a. 讀取車廂數量 L
   b. 讀取當前排列
   c. 計算逆序對數量
   d. 輸出結果字符串

=================================================================
範例展示：
=================================================================
【範例 1】[1, 3, 2]
  逆序對：(3, 2) → 共 1 個
  交換 1 次：[1,3,2] → [1,2,3]

【範例 2】[4, 3, 2, 1]
  逆序對：(4,3), (4,2), (4,1), (3,2), (3,1), (2,1) → 共 6 個
  需 6 次交換才能變成 [1,2,3,4]

【範例 3】[1, 2, 3, 4]
  沒有逆序對 → 需 0 次交換

=================================================================
"""

import sys

def count_inversions(arr):
    """
    計算陣列中的逆序對數量
    
    ===================================================================
    函數用途：
    ===================================================================
    通過計算陣列中的逆序對總數，來確定最少相鄰交換次數
    
    ===================================================================
    定義：
    ===================================================================
    逆序對 (i, j)：
    - 位置 i < j（i 在前，j 在後）
    - 但數值 arr[i] > arr[j]（前面的數更大）
    
    例如在 [3, 1, 2] 中：
    - (3, 1) 是逆序對（3 > 1，且 3 在前）
    - (3, 2) 是逆序對（3 > 2，且 3 在前）
    - (1, 2) 不是逆序對（1 < 2）
    總共 2 個逆序對
    
    ===================================================================
    參數：
    ===================================================================
    arr (list): 包含數字的排列
    
    ===================================================================
    返回值：
    ===================================================================
    int: 逆序對的總數（就是最少交換次數）

    ===================================================================
    時間複雜度：O(n²)
      兩層迴圈檢查所有 (i, j) 對
    
    空間複雜度：O(1)
      只使用一個計數變數
    
    ===================================================================
    演算法步驟：
    ===================================================================
    1. 初始化計數器 count = 0
    2. 外層迴圈：i = 0 到 n-2
    3. 內層迴圈：j = i+1 到 n-1
    4. 若 arr[i] > arr[j]，count += 1
    5. 返回 count
    """
    
    # ───── 初始化 ─────
    count = 0  # 逆序對計數器
    n = len(arr)  # 陣列長度
    
    # ───── 兩層迴圈檢查所有數對 ─────
    # 外層：選擇第一個元素的位置
    for i in range(n):
        # 內層：選擇第二個元素的位置（必須在第一個之後）
        for j in range(i + 1, n):
            # ───── 檢查是否為逆序對 ─────
            # 若前面的元素比後面的大，就是一個逆序對
            if arr[i] > arr[j]:
                count += 1
    
    # ───── 返回逆序對總數 ─────
    return count

# ============================================================================
# 主程式：讀取輸入並處理所有測資
# ============================================================================

def main():
    """
    ===================================================================
    主程式流程：
    ===================================================================
    1. 讀取測資數量 N
    2. 對每組測資進行循環：
       a. 讀取列車長度 L
       b. 讀取列車的當前排列
       c. 計算需要的最少交換次數
       d. 輸出結果
    
    ===================================================================
    輸入格式：
    ===================================================================
    第一行：N（測資數量）
    對於每組測資：
      - 第一行：L（列車長度，0 ≤ L ≤ 50）
      - 第二行：1 到 L 的排列（空格分隔）

    ===================================================================
    輸出格式：
    ===================================================================
    "Optimal train swapping takes S swaps."
    其中 S 為最少交換次數

    ===================================================================
    """
    
    # ───── 讀取測資數量 ─────
    # input() 讀取並移除末尾的換行符
    n = int(input())
    
    # ───── 處理每組測資 ─────
    # 迴圈執行 n 次，每次處理一組完整的測資
    for _ in range(n):
        # ───── 讀取列車長度 ─────
        # L 代表有多少節車廂
        l = int(input())
        
        # ───── 讀取列車當前排列 ─────
        # input() 讀取包含數字的整行字符串
        # split() 以空格分割字符串，得到字符串列表
        # map(int, ...) 將每個字符串轉換為整數
        # list(...) 將 map 物件轉換為列表
        if l > 0:
            trains = list(map(int, input().split()))
        else:
            # 特殊情況：L = 0 時列車為空，無需進一步處理
            trains = []
        
        # ───── 計算逆序對（最少交換次數） ─────
        swaps = count_inversions(trains)
        
        # ───── 輸出結果 ─────
        # 按照題目要求的格式輸出
        # 格式：Optimal train swapping takes S swaps.
        print(f"Optimal train swapping takes {swaps} swaps.")

# ============================================================================
# 程式入口點
# ============================================================================

if __name__ == '__main__':
    # ═════════════════════════════════════════════════════════════════
    # 執行主程式
    # ═════════════════════════════════════════════════════════════════
    main()
