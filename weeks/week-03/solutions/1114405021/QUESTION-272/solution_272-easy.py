"""
UVA 272 TeX 引號轉換 - 簡易版本

=================================================================
問題描述：
=================================================================
將文本中的普通雙引號 (") 轉換為 LaTeX/TeX 格式的引號。
- 開始引號（奇數個 "）：替換為 `` （兩個左單引號）
- 結束引號（偶數個 "）：替換為 '' （兩個右單引號）

=================================================================
解題思路 (使用狀態機模式)：
=================================================================
1. 維護一個布爾狀態變數 in_quote，記錄目前是否在引號內部
2. 逐字讀取輸入文本：
   - 如果遇到普通字符，直接複製到輸出
   - 如果遇到雙引號 "：
     • 若 in_quote = False (不在引號內)：
       - 這是「開始引號」（奇數個）
       - 替換為 `` （兩個左單引號）
       - 設置 in_quote = True（進入引號狀態）
     • 若 in_quote = True (在引號內)：
       - 這是「結束引號」（偶數個）
       - 替換為 '' （兩個右單引號）
       - 設置 in_quote = False（離開引號狀態）

=================================================================
時間複雜度：O(n)，其中 n 是輸入文本的總長度
空間複雜度：O(n)，用於儲存轉換結果
=================================================================

範例轉換：
輸入：  He said "Hello world"
輸出：  He said ``Hello world''

輸入：  She said "Yes" and he said "No"
輸出：  She said ``Yes'' and he said ``No''
"""

import sys

# ===== 初始化引號狀態變數 =====
# in_quote 用於追蹤目前是否在引號內部
# False：不在引號內（下一個 " 是開始引號）
# True：在引號內（下一個 " 是結束引號）
in_quote = False

# ===== 主程式迴圈：逐行讀取輸入 =====
# 使用 sys.stdin 讀取來自標準輸入的所有行
# 程式將持續讀取直到達到檔案結尾 (EOF)
for line in sys.stdin:
    # 儲存轉換後該行的字符
    result = []
    
    # ===== 逐字符處理該行 =====
    for char in line:
        # ===== 檢查是否為雙引號 =====
        if char == '"':
            # ===== 根據當前狀態決定替換方式 =====
            if in_quote:
                # 在引號內：這是結束引號（偶數個）
                # 替換為兩個右單引號 ''
                result.append("''")
                # 更新狀態：離開引號內部
                in_quote = False
            else:
                # 不在引號內：這是開始引號（奇數個）
                # 替換為兩個左單引號 ``
                result.append("``")
                # 更新狀態：進入引號內部
                in_quote = True
        else:
            # ===== 非雙引號字符 =====
            # 直接複製到輸出（保留原樣）
            # 包括：字母、數字、空格、標點符號等
            result.append(char)
    
    # ===== 輸出轉換後的該行 =====
    # ''.join(result)：將 result 列表中的所有字符連接成字符串
    # end=''：不添加換行符（原始換行符已包含在 line 中）
    print(''.join(result), end='')
