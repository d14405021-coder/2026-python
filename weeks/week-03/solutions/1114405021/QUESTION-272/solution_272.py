"""
UVA 272 TeX 引號轉換問題 - 完整解決方案

=================================================================
問題描述 (UVA Online Judge - Problem 272)：
=================================================================
將文本中的普通雙引號 (") 轉換為 TeX 格式的引號。

在 TeX 排版系統中，引號的寫法與普通文本不同：
- 左引號（開始引號）：用 `` 表示（兩個左單引號）
- 右引號（結束引號）：用 '' 表示（兩個右單引號）

問題要求：
- 輸入：包含普通雙引號的文本行（EOF 結尾）
- 輸出：將雙引號替換為 TeX 格式的文本

規則：
- 第 1 個、3 個、5 個...奇數個 " → 替換為 `` （開始引號）
- 第 2 個、4 個、6 個...偶數個 " → 替換為 '' （結束引號）
- 所有其他字符保持不變

=================================================================
核心演算法 (狀態機模式)：
=================================================================
使用狀態變數 in_quote 追蹤當前位置是否在引號內部：

1. 初始化：in_quote = False（還未進入任何引號）
2. 逐字符掃描輸入文本：
   - 遇到普通字符：直接輸出
   - 遇到雙引號 "：
     ├─ 如果 in_quote = False：
     │  ├─ 這是「開始引號」（第 1、3、5... 個）
     │  ├─ 替換為 ``
     │  └─ 設置 in_quote = True（進入引號)
     └─ 如果 in_quote = True：
        ├─ 這是「結束引號」（第 2、4、6... 個）
        ├─ 替換為 ''
        └─ 設置 in_quote = False（離開引號）

=================================================================
複雜度分析：
=================================================================
時間複雜度：O(n)
- 其中 n 是輸入文本的總字符數
- 每個字符恰好處理一次

空間複雜度：O(n)
- 用於儲存轉換後的結果字符串

=================================================================
實現特點：
=================================================================
- 使用函數封裝轉換邏輯（便於測試和重用）
- 採用列表 + join() 方式進行字符串拼接（比 += 高效）
- 支持多行輸入並保留原始換行符
"""

import sys

def convert_quote_to_tex(text):
    """
    將文本中的普通雙引號轉換為 TeX 格式的引號。
    
    ===================================================================
    函數功能：
    ===================================================================
    本函數實現引號轉換的核心邏輯。給定任意文本字符串，將其中的所有
    普通雙引號 (") 替換為 TeX 格式的引號：
    - 開始引號（奇數個）：替換為 `` （backtick 字符）
    - 結束引號（偶數個）：替換為 '' （apostrophe 字符）

    ===================================================================
    參數：
    ===================================================================
    text (str): 
        包含普通雙引號 (") 的原始文本。
        可以是單行或多行字符串（包含 \\n）。
        假設：引號數量為偶數（每個開始引號都對應一個結束引號）。

    ===================================================================
    返回值：
    ===================================================================
    str: 轉換後的文本字符串，其中：
        - 所有普通雙引號 (") 已替換為 TeX 格式
        - 所有其他字符保持不變（包括空格、換行符等）
        - 所有英文字母、數字、標點符號保持不變

        例如：
        input:  He said "Hello" to me.
        output: He said ``Hello'' to me.

    ===================================================================
    轉換卡過程詳解：
    ===================================================================
    狀態機轉移圖：
    
    [不在引號內] --遇到"--> [替換為``] --> [進入引號內]
                            
    [在引號內] --遇到"--> [替換為''] --> [退出引號內]

    變數說明：
    - result (list)：用於儲存轉換後的字符
    - in_quote (bool)：
      * False = 當前不在引號內（下一個 " 是開始引號）
      * True = 當前在引號內（下一個 " 是結束引號）

    ===================================================================
    時間複雜度：O(n)，其中 n 是文本長度
    空間複雜度：O(n)，用於儲存結果字符串
    
    ===================================================================
    使用範例：
    ===================================================================
    >>> convert_quote_to_tex('He said "Hello"')
    'He said ``Hello\'\''
    
    >>> convert_quote_to_tex('"Yes" and "No"')
    '``Yes\'\' and ``No\'\''
    """
    # ===== 初始化 =====
    result = []  # 存儲轉換後的字符（使用列表而非字符串以提高效率）
    in_quote = False  # 追蹤是否在引號內部（初始狀態：不在引號內）

    # ===== 逐字符掃描文本 =====
    for char in text:
        # ===== 檢查當前字符是否為雙引號 =====
        if char == '"':
            # ===== 引號轉換：根據狀態決定替換方式 =====
            if in_quote:
                # ─────────────────────────────────────────
                # 情況一：in_quote = True（在引號內）
                # ─────────────────────────────────────────
                # 當前遇到的 " 是「結束引號」（偶數個）
                # 應該替換為 '' （兩個右單引號/撇號）
                result.append("''")
                # 更新狀態：離開引號內部
                in_quote = False
            else:
                # ─────────────────────────────────────────
                # 情況二：in_quote = False（不在引號內）
                # ─────────────────────────────────────────
                # 當前遇到的 " 是「開始引號」（奇數個）
                # 應該替換為 `` （兩個左單引號/反引號）
                result.append("``")
                # 更新狀態：進入引號內部
                in_quote = True
        else:
            # ===== 非雙引號字符：直接保留 =====
            # 包括：
            # - 字母和數字（a-z, A-Z, 0-9）
            # - 空格和製表符（ , \\t）
            # - 英文標點（. , ! ? ; : - 等）
            # - 其他任何 Unicode 字符
            result.append(char)

    # ===== 字符串拼接與返回 =====
    # ''.join(result)：將列表中的所有元素連接成一個字符串
    # 這個方法比使用 += 逐次拼接更高效（避免重複複製）
    return ''.join(result)

# ============================================================================
# 主程式：讀取輸入並逐行轉換
# ============================================================================

# 使用 try-except 來正確處理文件末尾（EOF）
try:
    # ===== 主迴圈：持續讀取輸入行 =====
    # 程式將一直讀取 stdin 中的行，直到達到 EOF
    while True:
        # ───────────────────────────────────────────
        # 1. 讀取一行輸入（不包含換行符）
        # ───────────────────────────────────────────
        line = input()
        
        # ───────────────────────────────────────────
        # 2. 轉換該行中的引號
        # ───────────────────────────────────────────
        converted = convert_quote_to_tex(line)
        
        # ───────────────────────────────────────────
        # 3. 輸出轉換結果（自動添加換行符）
        # ───────────────────────────────────────────
        print(converted)
        
except EOFError:
    # ===== 異常處理：檔案結尾 =====
    # EOFError：當 input() 無法讀取更多行時拋出
    # （已到達輸入流的末尾）
    # 捕捉此異常並結束程式（不做任何操作）
    pass