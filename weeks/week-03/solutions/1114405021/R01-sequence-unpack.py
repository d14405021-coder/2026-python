# R1. 序列解包（1.1）

# 序列解包（unpacking）= 一次把序列中的多個元素，指派給多個變數。
# 常見可解包型別：tuple、list、str（以及其他可迭代物件）。
# 核心規則：左邊變數數量通常要和右邊元素數量一致。

# 1) 基本 tuple 解包
p = (4, 5)
# x 會拿到第一個元素 4，y 會拿到第二個元素 5
x, y = p

# 2) list 解包（元素可混合不同型別）
data = ['ACME', 50, 91.1, (2012, 12, 21)]

# 按順序把 data 的四個元素分別放進四個變數
name, shares, price, date = data

# 3) 巢狀解包：第四格本身是 tuple，也可再拆一層
name, shares, price, (year, mon, day) = data
# 完成後：year=2012, mon=12, day=21

# 丟棄不需要的值（占位）
# 慣例用 _ 表示「這個值我不打算使用」
_, shares, price, _ = data

