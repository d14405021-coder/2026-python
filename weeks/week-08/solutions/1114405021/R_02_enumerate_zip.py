# Remember（記憶）- enumerate() 和 zip()

# 這個檔案的目的，是用最直接的範例讓你觀察兩個常用工具：
# 1. enumerate()：同時拿到「索引」和「元素」
# 2. zip()：把多個序列一一對齊，組成一組一組的資料
#
# 下面的範例都會直接印出結果，方便你一邊看程式、一邊對照輸出內容。

colors = ["red", "green", "blue"]

# ------------------------------------------------------------
# enumerate() 的基本用法
# ------------------------------------------------------------
# enumerate(iterable) 會回傳「索引 + 值」的配對：
# - 第 1 個值是索引，預設從 0 開始
# - 第 2 個值是資料本身
# 這樣就不用自己另外宣告計數變數，也不用手動 i += 1。
print("--- enumerate() 基本用法 ---")
for i, color in enumerate(colors):
    print(f"{i}: {color}")

# ------------------------------------------------------------
# enumerate(start=1) 的用途
# ------------------------------------------------------------
# 很多時候人類習慣從 1 開始數，例如第 1 筆、第 2 筆、第 3 筆。
# 這時就可以把 start 設成 1，讓輸出的編號更符合閱讀習慣。
print("\n--- enumerate(start=1) ---")
for i, color in enumerate(colors, 1):
    print(f"第{i}個: {color}")

# ------------------------------------------------------------
# enumerate() 搭配檔案或文字行的情境
# ------------------------------------------------------------
# 當你讀檔後，常常會得到一串「逐行資料」。
# 這時 enumerate() 很適合拿來同時顯示「行號」與「內容」。
print("\n--- enumerate with 檔案 ---")
lines = ["line1", "line2", "line3"]
for lineno, line in enumerate(lines, 1):
    print(f"行 {lineno}: {line}")

# ------------------------------------------------------------
# zip() 的基本用法
# ------------------------------------------------------------
# zip() 會把多個序列「依照相同位置」配對起來。
# 例如 names[0] 會和 scores[0] 配成一組，names[1] 會和 scores[1] 配成一組。
print("\n--- zip() 基本用法 ---")
names = ["Alice", "Bob", "Carol"]
scores = [90, 85, 92]
for name, score in zip(names, scores):
    print(f"{name}: {score}")

# ------------------------------------------------------------
# zip() 配對三個序列
# ------------------------------------------------------------
# zip 不限於兩個序列，也可以一次配對多個序列。
# 這在整理對照表、同時處理多欄資料時非常常見。
print("\n--- zip() 多個序列 ---")
a = [1, 2, 3]
b = [10, 20, 30]
c = [100, 200, 300]
for x, y, z in zip(a, b, c):
    print(f"{x} + {y} + {z} = {x + y + z}")

# ------------------------------------------------------------
# zip() 在長度不同時的行為
# ------------------------------------------------------------
# 預設情況下，zip() 只會配對到最短的序列結束。
# 也就是說，如果其中一個序列比較短，後面多出來的值會被忽略。
print("\n--- zip() 長度不同 ---")
x = [1, 2]
y = ["a", "b", "c"]
print(f"list(zip(x, y)): {list(zip(x, y))}")

# 如果你希望「較短的序列」也能補值延長，就可以改用 zip_longest。
# 這裡把缺少的值用 0 補上，讓結果長度對齊到最長序列。
from itertools import zip_longest

print(f"zip_longest: {list(zip_longest(x, y, fillvalue=0))}")

# ------------------------------------------------------------
# zip() 常見應用：建立字典
# ------------------------------------------------------------
# 當兩個序列分別代表「鍵」與「值」時，
# 可以先用 zip() 配成成對資料，再交給 dict() 轉成字典。
# 這個技巧在整理欄位、讀設定值、對照表時都很常用。
print("\n--- 建立字典 ---")
keys = ["name", "age", "city"]
values = ["John", "30", "NYC"]
d = dict(zip(keys, values))
print(f"dict: {d}")
